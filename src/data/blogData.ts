export function getTodayData(): DayData | null {
  const today = new Date();
  // Номер дня года (0–365)
  const start = new Date(today.getFullYear(), 0, 0);
  const diff = today.getTime() - start.getTime();
  const dayOfYear = Math.floor(diff / 86400000);
  // Ротация по 14 записям (2 недели), привязанная к дню недели внутри блока
  const dayOfWeek = today.getDay() === 0 ? 6 : today.getDay() - 1; // 0=пн … 6=вс
  const week = Math.floor(dayOfYear / 7) % 2; // 0 или 1 — чётная/нечётная неделя
  const index = week * 7 + dayOfWeek;
  return blogData[index] ?? blogData[dayOfWeek];
}

export function getFormattedDate(): string {
  const today = new Date();
  const months = [
    "января", "февраля", "марта", "апреля", "мая", "июня",
    "июля", "августа", "сентября", "октября", "ноября", "декабря",
  ];
  return `${today.getDate()} ${months[today.getMonth()]} ${today.getFullYear()}`;
}